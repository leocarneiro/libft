#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*str;

	str = (const char *)s;
	while (n > 0)
	{
		if (*str == c)
			return ((void *)(str++));
		str++;
		n--;
	}
	return (NULL);
}

#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	j;

	j = n;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		j = j * (-1);
	}
	if (j / 10 > 0)
		ft_putnbr_fd(j / 10, fd);
	ft_putchar_fd((j % 10) + 48, fd);
}
